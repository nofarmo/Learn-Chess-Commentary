"""# Dataset"""

dataset = MovesDataset([f'{games_data_path}{i+1}.p' for i in range(config['NUMER_OF_DATA_DIRS'])], tokenizer, max_length=max_length)

train_size = int(config['train_precentege'] * len(dataset))
